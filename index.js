'use strict';

/**
 * Module dependencies.
 */

var fs = require('fs');
var utils = require('engine-utils');
var delimsEscape = require('escape-delims');
var Delimiters = require('delims');
var delimiters = new Delimiters();
var _ = require('lodash');


/**
 * Lodash support.
 */

var engine = utils.fromStringRenderer('lodash');


/**
 * Lodash string support. Render the given `str` and invoke the callback `callback(err, str)`.
 *
 * ```js
 * var engine = require('engine-lodash');
 * engine.render('<%= name %>', {name: 'Jon Schlinkert'}, function (err, content) {
 *   console.log(content); //=> 'Jon Schlinkert'
 * });
 * ```
 *
 * @param {String} `str`
 * @param {Object|Function} `options` or callback.
 *     @property {Object} `cache` enable template caching
 *     @property {String} `filename` filename required for caching
 * @param {Function} `callback`
 * @api public
 */

engine.render = function render(str, options, callback) {
  if (typeof options === 'function') {
    callback = options;
    options = {};
  }

  var opts = _.extend({}, options);
  if (!opts.noescape) {
    str = delimsEscape.escape(str);
  }

  var settings = {};

  _.extend(settings, opts);
  _.extend(settings, opts.settings);
  settings.imports = opts.imports || opts.helpers || {};

  if (opts.mixins) {
    _.mixin(opts.mixins);
  }

  if (opts.delims) {
    var delims = delimiters.templates(opts.delims);
    _.extend(settings, delims, opts);
  }

  try {
    var tmpl = utils.cache(opts) || utils.cache(opts, _.template(str, null, settings));
    var rendered = tmpl(opts);

    if (opts.delims) {
      rendered = delimsEscape.unescape(rendered);
    }

    // Pass file extension for use in assemble v0.6.x
    callback(null, rendered, '.html');
  } catch (err) {
    callback(err);
  }
};



/**
 * Render Lo-Dash or underscore templates synchronously.
 *
 * @param  {Object} `str` The string to render.
 * @param  {Object} `options` Object of options.
 *   @option {Object} `settings` Settings to pass to Lo-Dash.
 *   @option {Object} `delims` Template delimiters, generated by [delims]
 *   @option {Object} `imports` Template helpers to pass to Lo-Dash.
 * @return {String} Rendered string.
 * @api public
 */

engine.renderSync = function renderSync(str, options) {
  var opts = _.extend({}, options);

  if (!opts.noescape) {
    str = delimsEscape.escape(str);
  }

  var settings = {};

  _.extend(settings, opts);
  _.extend(settings, opts.settings);
  settings.imports = opts.imports || opts.helpers || {};

  if (opts.mixins) {
    _.mixin(opts.mixins);
  }

  if (opts.delims) {
    var delims = delimiters.templates(opts.delims);
    _.extend(settings, delims, opts);
  }

  try {
    var tmpl = utils.cache(opts) || utils.cache(opts, _.template(str, null, settings));
    var rendered = tmpl(opts);

    if (opts.delims) {
      rendered = delimsEscape.unescape(rendered);
    }

    return rendered;
  } catch (err) {
    return err;
  }
};


/**
 * Lodash file support. Render a file at the given `filepath` and callback `callback(err, str)`.
 *
 * @param {String} `path`
 * @param {Object|Function} `options` or callback function.
 * @param {Function} `callback`
 * @api public
 */

engine.renderFile = function renderFile(filepath, options, callback) {
  if (typeof options === 'function') {
    callback = options;
    options = {};
  }

  var opts = _.extend({}, options);
  try {
    var str;
    if (opts.cache) {
      str = engine.cache[filepath] || (engine.cache[filepath] = fs.readFileSync(filepath, 'utf8'));
    } else {
      str = fs.readFileSync(filepath, 'utf8');
    }

    engine.render(str, opts, callback);
  } catch (err) {
    callback(err);
  }
};

engine.cache = {};


/**
 * Express support.
 */

engine.__express = engine.renderFile;


/**
 * Expose `engine`
 */

module.exports = engine;